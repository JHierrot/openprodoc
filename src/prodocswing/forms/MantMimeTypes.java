public Record getRecord()
{
return MimeType;
}
//----------------------------------------------------------------
/**
* @param pUser the User to set
*/
public void setRecord(Record pUser)
{
MimeType = pUser;
Attribute Attr=MimeType.getAttr(PDMimeType.fNAME);
MimeNameLabel.setText(Attr.getUserName());
if (Attr.getValue()!=null)
    MimeNameTextField.setText((String)Attr.getValue());
MimeNameTextField.setToolTipText(Attr.getDescription());
Attr=MimeType.getAttr(PDMimeType.fDESCRIPTION);
DescriptionLabel.setText(Attr.getUserName());
if (Attr.getValue()!=null)
    DescriptionTextField.setText((String)Attr.getValue());
DescriptionTextField.setToolTipText(Attr.getDescription());
Attr=MimeType.getAttr(PDMimeType.fEXTENSION);
ExtensionLabel.setText(Attr.getUserName());
if (Attr.getValue()!=null)
    ExtTextField.setText((String)Attr.getValue());
ExtTextField.setToolTipText(Attr.getDescription());
}
