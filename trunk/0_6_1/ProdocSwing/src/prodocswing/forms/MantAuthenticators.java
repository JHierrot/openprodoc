    private void ButtonAceptActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_ButtonAceptActionPerformed
    {//GEN-HEADEREND:event_ButtonAceptActionPerformed
try {
Attribute Attr = Authent.getAttr(PDAuthenticators.fNAME);
Attr.setValue(RepNameTextField.getText());
Attr = Authent.getAttr(PDAuthenticators.fDESCRIPTION);
Attr.setValue(DescriptionTextField.getText());
Attr = Authent.getAttr(PDAuthenticators.fAUTHTYPE);
Attr.setValue(TypeComboBox.getSelectedItem());
Attr = Authent.getAttr(PDAuthenticators.fURL);
Attr.setValue(URLTextField.getText());
Attr = Authent.getAttr(PDAuthenticators.fPARAM);
Attr.setValue(ParamTextField.getText());
Attr = Authent.getAttr(PDAuthenticators.fUSERNAME);
Attr.setValue(UsrTextField.getText());
Attr = Authent.getAttr(PDAuthenticators.fPASSWORD);
Attr.setValue(PasswordTextField.getText());
Cancel = false;
this.dispose();
} catch (PDException ex)
    {
    MainWin.Message(MainWin.DrvTT(ex.getLocalizedMessage()));
    }
    }//GEN-LAST:event_ButtonAceptActionPerformed

