    private void ButtonAceptActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_ButtonAceptActionPerformed
    {//GEN-HEADEREND:event_ButtonAceptActionPerformed
try {
Attribute Attr = Custom.getAttr(PDCustomization.fNAME);
Attr.setValue(CustNameTextField.getText());
Attr = Custom.getAttr(PDCustomization.fDESCRIPTION);
Attr.setValue(DescriptionTextField.getText());
Attr = Custom.getAttr(PDCustomization.fLANGUAGE);
Attr.setValue(LangTextField.getText());
Attr = Custom.getAttr(PDCustomization.fDATEFORMAT);
Attr.setValue(DateTextField.getText());
Attr = Custom.getAttr(PDCustomization.fTIMEFORMAT);
Attr.setValue(TimeTextField.getText());
Attr = Custom.getAttr(PDCustomization.fSTYLE);
Attr.setValue(StileTextField.getText());
Attr = Custom.getAttr(PDCustomization.fSWINGSTYLE);
Attr.setValue(SStyleTextField.getText());
Cancel = false;
this.dispose();
} catch (PDException ex)
    {
    MainWin.Message(MainWin.DrvTT(ex.getLocalizedMessage()));
    }
    }//GEN-LAST:event_ButtonAceptActionPerformed

