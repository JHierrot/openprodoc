/**
 * @param pCustom 
 */
public void setRecord(Record pCustom)
{
Custom = pCustom;
Attribute Attr=Custom.getAttr(PDCustomization.fNAME); //-------------------------
CustNameLabel.setText(MainWin.DrvTT(Attr.getUserName()));
if (Attr.getValue()!=null)
    CustNameTextField.setText((String)Attr.getValue());
CustNameTextField.setToolTipText(MainWin.DrvTT(Attr.getDescription()));
Attr=Custom.getAttr(PDCustomization.fDESCRIPTION);//--------------------------
DescriptionLabel.setText(MainWin.DrvTT(Attr.getUserName()));
if (Attr.getValue()!=null)
    DescriptionTextField.setText((String)Attr.getValue());
DescriptionTextField.setToolTipText(MainWin.DrvTT(Attr.getDescription()));
Attr=Custom.getAttr(PDCustomization.fLANGUAGE); //--------------------------
LangLabel.setText(MainWin.DrvTT(Attr.getUserName()));
if (Attr.getValue()!=null)
    LangTextField.setText((String)Attr.getValue());
LangTextField.setToolTipText(MainWin.DrvTT(Attr.getDescription()));
Attr=Custom.getAttr(PDCustomization.fDATEFORMAT); //--------------------------
DateLabel.setText(MainWin.DrvTT(Attr.getUserName()));
if (Attr.getValue()!=null)
    DateTextField.setText((String)Attr.getValue());
DateTextField.setToolTipText(MainWin.DrvTT(Attr.getDescription()));
Attr=Custom.getAttr(PDCustomization.fTIMEFORMAT); //--------------------------
TimeLabel.setText(MainWin.DrvTT(Attr.getUserName()));
if (Attr.getValue()!=null)
    TimeTextField.setText((String)Attr.getValue());
TimeTextField.setToolTipText(MainWin.DrvTT(Attr.getDescription()));
Attr=Custom.getAttr(PDCustomization.fSTYLE); //------------------------
StyleLabel.setText(MainWin.DrvTT(Attr.getUserName()));
if (Attr.getValue()!=null)
    StileTextField.setText((String)Attr.getValue());
StileTextField.setToolTipText(MainWin.DrvTT(Attr.getDescription()));
Attr=Custom.getAttr(PDCustomization.fSWINGSTYLE); //--------------------------
SStyleLabel.setText(MainWin.DrvTT(Attr.getUserName()));
if (Attr.getValue()!=null)
    SStyleTextField.setText((String)Attr.getValue());
SStyleTextField.setToolTipText(MainWin.DrvTT(Attr.getDescription()));
}
